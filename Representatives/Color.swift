import Foundation
import UIKit


extension UIColor {
    
    class func flatRedColor() -> UIColor {
        
        return UIColor(red: 0.878, green: 0.294, blue: 0.247, alpha: 1.00)
    }
    
    class func flatBlueColor() -> UIColor {
        
        return UIColor(red: 0.196, green: 0.518, blue: 0.780, alpha: 1.00)
    }
}